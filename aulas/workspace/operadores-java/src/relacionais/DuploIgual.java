package relacionais;

public class DuploIgual {
	public static void main(String[] args) {
		int x = 4;
		int y = 4;
		
		//imprimir o resultado da expressão x == y
		System.out.println(x == y);
		
		//Mudar o valor do x
		x = 3;
		
		//imprimir o resultado da expressão x == y
		System.out.println(x == y);

	}
}
